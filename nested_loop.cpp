#include <bits/stdc++.h>
int main () {
    
//Practicing Nested Loop :

    for (int i = 1; i <= 2; i++) {

        for (int j = 1; j <= 3; j++) {
            
            std::cout << i << " , " << j << std::endl;

        }
    }

    std::cout << "______________End of loop_____________" << std::endl;

return 0;

}
